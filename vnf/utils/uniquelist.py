def uniquelist(seq, idfun=None): 
    # order preserving
    if idfun is None:
        def idfun(x): return x
    seen = {}
    result = []
    for item in seq:
        marker = idfun(item)
        # in old Python versions:
        # if seen.has_key(marker)
        # but in new ones:
        if marker in seen: continue
        seen[marker] = 1
        result.append(item)
    return result


def test():
    assert uniquelist([1,2,2,3])==[1,2,3]
    return


def main():
    test()
    return

if __name__ == '__main__': main()


